//Variables Tutorial
package javaapplication2;

public class JavaApplication2 {

    public static void main(String[] args) {
        //Declaring and Initializing Variables
        
        //DECLARATION: Datatype VariableName = Value
        
        int age = 16; //int for number value
        double height = 5.8; //double for decimal
        char gender = 'M'; //char for character value
        boolean isCool = true; //boolean for a true or false value
        String name =  "Lukas Muench"; //String for a sequence of characters
        
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height + " feet");
        System.out.println("Gender: " + gender);
        System.out.println("Is Cool: " +  isCool);  
        
  
        
    }
    
}
